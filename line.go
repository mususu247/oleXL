package oleXL

import (
	"log"

	"github.com/go-ole/go-ole"
)

type workLine struct {
	app    *Excel
	parent any
	num    int
}

func (Q *workShape) Line() *workLine {
	var body workLine
	xl := Q.app

	name := "Line"
	core, num := xl.cores.FindAdd(name, Q.num)
	if core.disp == nil {
		cmd := "Get"
		ans, err := xl.cores.SendNum(cmd, name, Q.num, nil)
		if err != nil {
			log.Printf("(Error) %v", err)
			return nil
		}
		switch x := ans.(type) {
		case *ole.IDispatch:
			if x != nil {
				core.disp = x
				core.lock = 0
			} else {
				return nil
			}
		}
	}
	body.app = xl
	body.num = num
	body.parent = Q
	return &body
}

